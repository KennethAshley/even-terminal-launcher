const catalogs: Record<AppLocale, Record<TranslationKey, string>> = {
  ja,
  en,
  "zh-CN": zhCN,
  "zh-TW": zhTW,
  ko,
  es
};

export function resolveLocale(
  preference: LocalePreference,
  systemLocale: string
): AppLocale {
  if (preference !== "system") return preference;
  const normalized = systemLocale.toLowerCase();
  if (normalized.startsWith("ja")) return "ja";
  if (normalized.startsWith("zh-tw") || normalized.startsWith("zh-hk") || normalized.startsWith("zh-hant")) return "zh-TW";
  if (normalized.startsWith("zh")) return "zh-CN";
  if (normalized.startsWith("ko")) return "ko";
  if (normalized.startsWith("es")) return "es";
  return "en";
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  parameters: Record<string, string | number> = {}
): string {
  return Object.entries(parameters).reduce(
    (message, [name, value]) =>
      message.replaceAll(`{${name}}`, String(value)),
    catalogs[locale][key]
  );
}
