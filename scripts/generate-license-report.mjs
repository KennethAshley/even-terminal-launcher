import console from "node:console";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const lockfilePath = join(projectRoot, "package-lock.json");
const reportPath = join(
  projectRoot,
  "docs",
  "legal",
  "dependency-license-inventory.md"
);

const args = new Set(process.argv.slice(2));
if (
  (args.has("--write") && args.has("--check")) ||
  (!args.has("--write") && !args.has("--check"))
) {
  console.error("Use exactly one of --write or --check.");
  process.exit(2);
}

const lockfile = JSON.parse(await readFile(lockfilePath, "utf8"));
const rootPackage = lockfile.packages?.[""];
if (!rootPackage || !lockfile.packages) {
  throw new Error("package-lock.json does not contain npm lockfile package data");
}

const directDependencies = new Set(
  Object.keys(rootPackage.dependencies ?? {})
);

function packageName(packagePath, metadata) {
  if (metadata.name) {
    return metadata.name;
  }
  return packagePath.split("node_modules/").at(-1);
}

function reviewedLicense(name, reportedLicense) {
  if (name === "@evenrealities/even-terminal") {
    return "NOASSERTION (package README says MIT)";
  }
  if (name === "qrcode-terminal") {
    return "Apache-2.0 (upstream repository)";
  }
  if (
    name === "@anthropic-ai/claude-agent-sdk" ||
    name.startsWith("@anthropic-ai/claude-agent-sdk-")
  ) {
    return "LicenseRef-Anthropic-Commercial-Terms";
  }
  return reportedLicense ?? "NOASSERTION";
}

function platformLabel(metadata) {
  const parts = [];
  if (metadata.os?.length) {
    parts.push(`os: ${metadata.os.join(", ")}`);
  }
  if (metadata.cpu?.length) {
    parts.push(`cpu: ${metadata.cpu.join(", ")}`);
  }
  return parts.join("; ") || "all";
}

function compareText(left, right) {
  return left < right ? -1 : left > right ? 1 : 0;
}

const recordsByIdentity = new Map();
for (const [packagePath, metadata] of Object.entries(lockfile.packages)) {
  if (!packagePath || metadata.dev === true) {
    continue;
  }
  const name = packageName(packagePath, metadata);
  const record = {
    name,
    version: metadata.version,
    direct: directDependencies.has(name),
    license: reviewedLicense(name, metadata.license),
    platform: platformLabel(metadata)
  };
  const identity = [
    record.name,
    record.version,
    record.license,
    record.platform
  ].join("\0");
  recordsByIdentity.set(identity, record);
}

const records = [...recordsByIdentity.values()].sort(
  (left, right) =>
    compareText(left.name, right.name) ||
    compareText(left.version, right.version) ||
    compareText(left.platform, right.platform)
);

const counts = new Map();
for (const record of records) {
  counts.set(record.license, (counts.get(record.license) ?? 0) + 1);
}

const escapeCell = (value) => String(value).replaceAll("|", "\\|");
const summaryRows = [...counts.entries()]
  .sort(([left], [right]) => compareText(left, right))
  .map(
    ([license, count]) => `| ${escapeCell(license)} | ${count} |`
  )
  .join("\n");
const inventoryRows = records
  .map(
    (record) =>
      `| ${escapeCell(record.name)} | ${escapeCell(record.version)} | ${
        record.direct ? "direct" : "transitive"
      } | ${escapeCell(record.license)} | ${escapeCell(record.platform)} |`
  )
  .join("\n");

const report = `# Production dependency license inventory

Generated from \`package-lock.json\` by
\`scripts/generate-license-report.mjs\`. Do not edit this file manually.

Scope: the production dependency closure recorded by npm (entries not marked
\`dev: true\`), including optional platform packages for macOS, Windows, and
other platforms present in the lockfile. Duplicate package/version/platform
records are collapsed. This is an engineering inventory, not legal advice.

## Reviewed exceptions

- \`@evenrealities/even-terminal@0.8.1\`: package metadata and tarball do not
  contain an authoritative license, although the package README says MIT.
- \`@anthropic-ai/claude-agent-sdk\` and platform packages: use is subject to
  Anthropic's legal/commercial terms rather than an open-source SPDX license.
- \`qrcode-terminal@0.12.0\`: npm metadata omits the license; the upstream
  repository supplies Apache-2.0 plus an additional bundled-code notice.

See \`THIRD_PARTY_NOTICES.md\` for sources and redistribution cautions.

## Summary

| Reviewed license expression | Package records |
|---|---:|
${summaryRows}

## Inventory

| Package | Version | Relationship | Reviewed license | Platform |
|---|---:|---|---|---|
${inventoryRows}
`;

if (args.has("--write")) {
  await mkdir(dirname(reportPath), { recursive: true });
  await writeFile(reportPath, report);
  console.log(`Updated ${reportPath}`);
} else {
  let current;
  try {
    current = await readFile(reportPath, "utf8");
  } catch {
    console.error("License inventory is missing. Run npm run license:report.");
    process.exit(1);
  }
  const normalizedCurrent = current.replaceAll("\r\n", "\n");
  if (normalizedCurrent !== report) {
    console.error(
      "License inventory is stale. Run npm run license:report and review it."
    );
    process.exit(1);
  }
  console.log(`License inventory is current (${records.length} records).`);
}
